uint32_t _SPD_CONST = 0;
uint32_t _RPM_CONST = 0;
void spd_setup (uint8_t p1, uint8_t p2, uint8_t p3)
{
    _SPD_CONST = (p1 * p2 * 2 * 314 * 36 + p3 * 254 * 3141 * 36) / 100000;
}
void rpm_setup (uint8_t rpe)
{
    _RPM_CONST = 6000 * rpe; // 60000 si 4 led
}

